# Split the data into lines
$lines = $data -split "`n"

# Process each line
foreach ($line in $lines) {
    if ($line.Trim() -eq "") { continue }
    $parts = $line -split " ", 2
    $range = $parts[0]
    $icons = $parts[1] -split " " | Where-Object { $_ -ne "" }

    # Print the range as a better-formatted label with color and padding
    Write-Host ("${ansiColor} {0,-15} :" -f $range) -NoNewline

    # Print each icon with a unique random 24-bit ANSI color and fixed spacing (2 spaces between icons for perfect alignment)
    for ($i = 0; $i -lt $icons.Count; $i++) {
        $R = Get-Random -Min 0 -Max 256
        $G = Get-Random -Min 0 -Max 256
        $B = Get-Random -Min 0 -Max 256
        $ansiColor = "`e[38;2;${R};${G};${B}m"
        Write-Host "${ansiColor}  $($icons[$i])`e[0m" -NoNewline
    }

    # New line after the row
    Write-Host ""
}
